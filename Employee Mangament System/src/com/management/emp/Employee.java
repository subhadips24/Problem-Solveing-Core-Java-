package com.management.emp;

public class Employee {
	
	public int id;
	public String name;
	public double salary;
	public String place;
	public String designation;
	@Override
	public String toString() {
		return "Employee [Id=" + id + ", Name=" + name + ", Salary=" + salary + ", Place=" + place + ", Designation="
				+ designation + "]";
	}
	
	
	
	
	
	
	

}
